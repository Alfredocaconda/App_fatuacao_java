public class Mvenda {

    private String codigo;
    private String quantidaderequerida;
    private String totalcompra;
    private String datavenda;
    private String fatura;
    private String idfuncionario;
    private String idproduto;

    public String getIdfuncionario() {
        return idfuncionario;
    }

    public void setIdfuncionario(String idfuncionario) {
        this.idfuncionario = idfuncionario;
    }

    public String getIdproduto() {
        return idproduto;
    }

    public void setIdproduto(String idproduto) {
        this.idproduto = idproduto;
    }

    public String getCodigo() {
        return codigo;
    }

    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    public String getQuantidaderequerida() {
        return quantidaderequerida;
    }

    public void setQuantidaderequerida(String quantidaderequerida) {
        this.quantidaderequerida = quantidaderequerida;
    }

    public String getTotalcompra() {
        return totalcompra;
    }

    public void setTotalcompra(String totalcompra) {
        this.totalcompra = totalcompra;
    }

    public String getDatavenda() {
        return datavenda;
    }

    public void setDatavenda(String datavenda) {
        this.datavenda = datavenda;
    }

    public String getFatura() {
        return fatura;
    }

    public void setFatura(String fatura) {
        this.fatura = fatura;
    }

    
    
   

}
