    private void salvar() {
        Mdevedor f = new Mdevedor();
        Cdevedor c = new Cdevedor();
        String nome = txtnome.getText();
        String genero = (String) cbgenero.getSelectedItem();
        String bi = txtbi.getText();
        int telefone = Integer.valueOf(txttelefone.getText());
        String morada = txtmorada.getText();

        if (!nome.isEmpty() && !bi.isEmpty() && !genero.isEmpty()) {
            f.setBi(bi);
            f.setGenero(genero);
            f.setNome(nome);
            f.setMorada(morada);
            f.setTelefone(telefone);
            f.setFuncionario(Integer.valueOf(idf));
            c.salvar(f);
        } else {
            f.mensagem("PRENCHA OS ESPAÇOS VAZIOS");
        }

        LIMPAR();
        txtpagamento.setEnabled(false);
        txtdatapagamento.setEnabled(false);
        btnpagamento.setEnabled(false);
        pesquisar("");

    }

    private void atualizar() {
        Mdevedor f = new Mdevedor();
        Cdevedor c = new Cdevedor();
        String nome = txtnome.getText();
        String genero = (String) cbgenero.getSelectedItem();
        String bi = txtbi.getText();
        String morada = txtmorada.getText();
        int telefone = Integer.valueOf(txttelefone.getText());
        if (txtbi.getText().equals("") || txtnome.getText().equals("") || txttelefone.getText().equals("")
                || cbgenero.getSelectedIndex() == 0 || txtmorada.getText().equals("")) {
            f.mensagem("PRENCHA OS ESPAÇOS VAZIOS");
        } else {
            f.setBi(bi);
            f.setGenero(genero);
            f.setNome(nome);
            f.setTelefone(telefone);
            f.setMorada(morada);
            f.setFuncionario(Integer.valueOf(idf));
            c.actualizar(f);
        }

        pesquisar("");
        LIMPAR();

    }

    public int pesquisar(String pesquisar) {
        int qtd = 0;
        Cdevedor f = new Cdevedor();
        DefaultTableModel dt = (DefaultTableModel) tbcadastro.getModel();
        Object[] recebe = {"CODIGO", "NOME", "GENERO", "BI", "MORADA", "TELEFONE"};
        dt.setColumnIdentifiers(recebe);
        dt.setNumRows(0);
        for (Mdevedor m : f.pesquisar(pesquisar)) {
            Object[] dado = {m.getIdt(), m.getNome(), m.getGenero(), m.getBi(), m.getMorada(), m.getTelefone()};
            dt.addRow(dado);
            if (m.getIdt() > 0) {
                qtd++;
            }
        }
        return qtd;
    }

