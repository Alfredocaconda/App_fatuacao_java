    private void tbcadastroMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbcadastroMouseClicked

        //codigo para o duplo clique
        if (evt.getClickCount() == 2) {
            //codigo do Joptiona 
            //declaramos uma vetor objecto que vai armazenar os itens selecionado
            Object[] recebe = {"EDITAR", "ELIMINAR", "DIVIDA", "CANCELAR"};
            int valor = (int) JOptionPane.showOptionDialog(this, "O QUE DESEJA?", "AVISO",
                    JOptionPane.DEFAULT_OPTION, JOptionPane.PLAIN_MESSAGE, null, recebe, recebe[3]);
            if (valor == 0) {
                codigo = (int) tbcadastro.getValueAt(tbcadastro.getSelectedRow(), 0);
                txtnome.setText((String) tbcadastro.getValueAt(tbcadastro.getSelectedRow(), 1));
                cbgenero.setSelectedItem((String) tbcadastro.getValueAt(tbcadastro.getSelectedRow(), 2));
                txtbi.setText((String) tbcadastro.getValueAt(tbcadastro.getSelectedRow(), 3));
                txtmorada.setText((String) tbcadastro.getValueAt(tbcadastro.getSelectedRow(), 4));
                txttelefone.setText((String) "" + tbcadastro.getValueAt(tbcadastro.getSelectedRow(), 5));
                btnactualizar.setText("ACTUALIZAR");
                btnsalvar.setVisible(false);
                txtdatadivi.setEnabled(!false);
                txtdivida.setEnabled(!false);
                txtfatura.setEnabled(!false);
                id++;
                v = 0;
            } else {
                if (valor == 1) {
                    apagar();
                    pesquisar("");
                } else {
                    codigo = (int) tbcadastro.getValueAt(tbcadastro.getSelectedRow(), 0);
                    txtdivida.requestFocus();
                    v = 3;
                }
            }

        }

    }//GEN-LAST:event_tbcadastroMouseClicked

