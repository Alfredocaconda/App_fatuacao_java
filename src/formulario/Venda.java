    private void tbpesquisarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbpesquisarMouseClicked
        // TODO add your handling code here:
        id = (String) tbpesquisar.getValueAt(tbpesquisar.getSelectedRow(), 0);
        double preco = Double.valueOf((String) tbpesquisar.getValueAt(tbpesquisar.getSelectedRow(), 2));
        int qtdExistente = Integer.valueOf((String) tbpesquisar.getValueAt(tbpesquisar.getSelectedRow(), 3));
        String nome = (String) tbpesquisar.getValueAt(tbpesquisar.getSelectedRow(), 1);
        if (evt.getClickCount() == 2) {
            quantida = Integer.valueOf(JOptionPane.showInputDialog("DIGITE A QUANTIDADE PRETENDIDA"));
            if (quantida > 0 & quantida <= qtdExistente) {
                double valor = quantida * preco;
                total += valor;
                DefaultTableModel dt = (DefaultTableModel) tbfaturados.getModel();
                Object[] titulo = {"CODIGO", "NOME / DESCRIÇÃO /CATEGORIA", "QUANTIDADE", "SUBTOTAL"};
                dt.setColumnIdentifiers(titulo);
                Object[] recebe = {id, nome, quantida, valor};
                dt.addRow(recebe);
            } else {
                JOptionPane.showMessageDialog(this, "A QUANTIDADE PRETENDIDA NÃO PODE SER MENOR QUE ZERO"
                        + " E NEM MAIOR QUE A QUANTIDADE EXISTENTE NO STOCK");
            }
        }
        txttotalcompra.setText("TOTAL DA COMPRA: " + total + " KZ");

    }//GEN-LAST:event_tbpesquisarMouseClicked

