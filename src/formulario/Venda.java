    private void pesquisar(String pesquisar) {
        Cvenda f = new Cvenda();
        DefaultTableModel dt = (DefaultTableModel) tbpesquisar.getModel();
        Object[] recebe = {"CODIGO", "NOME / DESCRIÇÃO /CATEGORIA", "PREÇO", "QUANTIDADE", "CATEGÓRIA"};
        dt.setColumnIdentifiers(recebe);
        dt.setNumRows(0);
        for (Modal.MviewEntrada m : f.pesquisar(pesquisar)) {
            Object[] dado = {m.getCodigo(), m.getNome() + " " + m.getDescricao(), m.getPreco(),
                m.getQuantidade(), m.getCategoria()};
            dt.addRow(dado);
        }
    }

    private void pesquisarbarra(String pesquisar) {
        Cvenda f = new Cvenda();
        DefaultTableModel dtm = (DefaultTableModel) tbpesquisar.getModel();
        Object[] recebe = {"CODIGO", "NOME / DESCRIÇÃO /CATEGORIA", "PREÇO", "QUANTIDADE", "CATEGÓRIA"};
        dtm.setColumnIdentifiers(recebe);
        dtm.setNumRows(0);
        int preco = 0;
        int c = 0;
        String nome = "";
        for (Modal.MviewEntrada m : f.pesquisar(pesquisar)) {
            Object[] dado = {m.getCodigo(), m.getNome() + " " + m.getDescricao(), m.getPreco(),
                m.getQuantidade(), m.getCategoria()};
            id = m.getCodigo();
            nome = m.getNome();
            preco = Integer.valueOf(m.getPreco());
            dtm.addRow(dado);
            c++;
        }
        if (c != 0 & c < 2) {
            quantida = Integer.valueOf(JOptionPane.showInputDialog("DIGITE A QUANTIDADE PRETENDIDA"));
            if (quantida > 0) {
                int valor = quantida * preco;
                total += valor;
                DefaultTableModel dt = (DefaultTableModel) tbfaturados.getModel();
                Object[] titulo1 = {"CODIGO","NOME / DESCRIÇÃO /CATEGORIA", "QUANTIDADE", "SUBTOTAL"};
                dt.setColumnIdentifiers(titulo1);
                Object[] recebe1 = {id, nome, quantida, valor};
                dt.addRow(recebe1);

            } else {
                JOptionPane.showMessageDialog(this, "A QUANTIDADE PRETENDIDA NÃO PODE SER MENOR QUE ZERO E NEM MAIOR QUE A QUANTIDADE"
                        + "EXISTENTE NO STOCK");
            }
            txtpesquisar.setText("");
            txtpesquisar.requestFocus();
            txttotalcompra.setText("TOTAL DA COMPRA: " + total + " KZ");
        }

    }

    private String data() {
        Date d = new Date();
        SimpleDateFormat s = new SimpleDateFormat("yyyy/MM/dd HH:mm:ss");
        return s.format(d);
    }

    private String dataf() {
        Date d = new Date();
        SimpleDateFormat s = new SimpleDateFormat("yyyyMMddHHmmss");
        return s.format(d);
    }
    double dinheiro = 0, v = 0;

    private void troco(double pago) {
        v = pago;
        dinheiro = v - total;
        txttroco.setText("TROCO: " + dinheiro + " KZ");
    }

    private void finalizar() {
        double valor = Double.valueOf(txtvalorpago.getText());
        if (valor < total) {
            JOptionPane.showMessageDialog(this, "VALOR INSUFICIENTE");
            txtvalorpago.requestFocus();
        } else {
            vender();
        }
    }

    private void vender() {

        DefaultTableModel dtm = (DefaultTableModel) tbfaturados.getModel();
        String datav = txtdatavenda.getText();
        String fatura = dataf();
        for (int i = 0; i < tbfaturados.getRowCount(); i++) {
            String idp = (String) tbfaturados.getValueAt(i, 0);
            int qtdrequerida = (int) tbfaturados.getValueAt(i, 2);
            double totalcompra = (double) tbfaturados.getValueAt(i, 3);
            Mvenda m = new Mvenda();
            m.setDatavenda(datav);
            m.setFatura(fatura);
            m.setIdproduto(idp);
            m.setQuantidaderequerida("" + qtdrequerida);
            m.setTotalcompra("" + totalcompra);
            m.setIdfuncionario(idf);
            new Cvenda().salvar(m);

        }
        //LIMPAR CAMPOS

        new Cvenda().impressao(dinheiro, v, fatura, txtcliente.getText());
        total = 0;
        txtvalorpago.setText("");
        txtcliente.setText("");
        //txtcliente.requestFocus();
        txttotalcompra.setText("TROTAL DA COMPRA : 0 KZ");
        txttroco.setText("TROCO:  00 KZ");
        dtm.setNumRows(0);
        dinheiro = v = 0;
    }

